#AREA Y PERIMETRO DEL CUADRADO
print("Calulemos juntos el área y el perímetro un cuadrado")
lado = input("Ingrese el valor de un lado del cuadrado: ")
perimetro = float(lado) * 4
area = float(lado) * float(lado)
print("El área del cuadrado es " + str(area))
print("El perímetro del cuadrado es " + str(perimetro))
