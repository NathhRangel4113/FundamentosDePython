#AREA Y LONGITUD DE LA CIRCUNFERENCIA
#l= 2*(pi*r)
#a= pi*(r**2)
print("Calulemos juntos el área y la longitud de una circunferencia")
radio = input("Ingrese el valor del radio de la circunferencia: ")
pi= float(3.14159265358979323846)
longitud = 2*(float(pi)*float(radio))
area =float(pi)*(float(radio))**2
print("La longitud de la circunferencia es: " + str(longitud))
print("El área de la circunferencia es: " + str(area))
